/* main.js - interacción básica */
const $ = sel => document.querySelector(sel);
const $$ = sel => Array.from(document.querySelectorAll(sel));

document.addEventListener('DOMContentLoaded', () => {
  // Añadir años en footers
  ['#year-home','#year-ropa','#year-ord','#year-sart'].forEach(s => {
    const el = document.querySelector(s);
    if(el) el.textContent = new Date().getFullYear();
  });

  // Newsletter (simulado)
  $$('.newsletter-form').forEach(form => {
    form.addEventListener('submit', e => {
      e.preventDefault();
      const input = form.querySelector('input[type="email"]');
      if(!input || !input.value) return alert('Introduce tu email');
      const list = JSON.parse(localStorage.getItem('newsletter-list')||'[]');
      if(!list.includes(input.value)) list.push(input.value);
      localStorage.setItem('newsletter-list', JSON.stringify(list));
      input.value='';
      alert('Gracias por suscribirte (simulado).');
    });
  });

  // Contact forms (simulado)
  $$('#contact-form, #contact-form-ropa, #contact-form-sartenes').forEach(f => {
    f && f.addEventListener('submit', e => { e.preventDefault(); alert('Mensaje enviado (simulado).'); f.reset(); });
  });

  // Mark buy buttons without real href for easy editing
  $$('.buy-button').forEach(b => {
    if(!b.getAttribute('href') || b.getAttribute('href').startsWith('[')) {
      b.classList.add('needs-affiliate');
      b.title = 'Pegue su enlace de afiliado aquí';
    }
  });

  // Search forms: apply to the visible products area
  $$('form.search-form').forEach(frm => {
    frm.addEventListener('submit', e => {
      e.preventDefault();
      const q = frm.querySelector('input[type="search"]').value.trim().toLowerCase();
      if(!q) return;
      // buscar dentro de productos de la misma página
      const products = document.querySelectorAll('.product-card');
      products.forEach(card => {
        const text = (card.textContent||'').toLowerCase();
        card.style.display = text.includes(q) ? '' : 'none';
      });
    });
  });
});

